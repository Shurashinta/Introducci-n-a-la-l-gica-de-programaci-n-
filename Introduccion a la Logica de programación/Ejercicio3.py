# Determinar la edad de una persona conociendo el año actual y el año de nacimiento.



# Importar librerias o bibliotecas 

# Entrada
año_actual = int(input("Ingresa el año actual:"))
año_de_nacimiento = int(input("Ingresa el año de nacimiento:"))


# Proceso
edad_de_la_persona = año_actual - año_de_nacimiento 



# Salida
print ("La edad de la persona es =", edad_de_la_persona)